; Calculadora
PUBLIC X
PUBLIC Y
PUBLIC OP
PUBLIC RESULT
PUBLIC FINAL
SOMA_SUB: EXTERN
MAIN: BEGIN

SECTION DATA
TRES: CONST 3
X: SPACE
Y: SPACE
OP: SPACE
RESULT: SPACE

MUL_DIV: EXTERN

SECTION TEXT


; Início do programa
INPUT X ; Recebe x
INPUT Y ; Recebe y
INPUT OP; Recebe operação
; 1 SOMA
; 2 SUBTRAÇÃO
; 3 MUL
; 4 DIV

; Decide o módulo do pulo
LOAD OP
SUB TRES
JMPN SOMA_SUB
JMP MUL_DIV

FINAL: OUTPUT RESULT
STOP
END