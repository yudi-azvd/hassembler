; Soma e subtração 
SOMA_SUB: BEGIN


SECTION TEXT
X: EXTERN
Y: EXTERN
OP: EXTERN
RESULT: EXTERN
FINAL: EXTERN
PUBLIC SOMA_SUB

; Decide operação
LOAD OP
SUB DOIS
JMPZ SUBTRACAO

; Realiza soma
LOAD X
ADD Y
STORE RESULT
JMP FINAL

; Realiza subtração
SUBTRACAO: LOAD X
SUB Y
STORE RESULT
JMP FINAL


SECTION DATA
DOIS: CONST 2


END